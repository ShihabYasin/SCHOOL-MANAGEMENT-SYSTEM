    private void SEARCH_BUTTONActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SEARCH_BUTTONActionPerformed

        String name=SEARCH_TEXT_FIELD.getText();
        try{           OracleDataSource ods = new OracleDataSource();
            ods.setURL("jdbc:oracle:thin:@localhost:1521:XE");
            ods.setUser("shihab");
            ods.setPassword("123");
            Connection conn = ods.getConnection();
            Statement stmt = conn.createStatement();
            ResultSet rset = stmt.executeQuery ("SELECT b.STUDENT_ID, a.BANGLA,a.ENGLISH FROM  RESULT  a join STUDENT b on a.STUDENT_ID=b.STUDENT_ID  "
                    + " where   b.NAME='"+name+"'");
           
            String ID="";
            String bangla="";
            String english="";

            while (rset.next())

            {
                ID=rset.getString(1);
                bangla=rset.getString(2);
                english=rset.getString(3);

            }
            
            STUDENT_ID.setText(ID);
            BANGLA_TEXT.setText(bangla);
            ENGLISH_TEXT.setText(english);
            

        }
        catch(Exception a)
        {
            System.err.println(""+a);
        }

    }//GEN-LAST:event_SEARCH_BUTTONActionPerformed

